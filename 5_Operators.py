# operators in Python

# (15, 6) -> Operands and (+, -, *, /) -> Operators

print(15+6) # addition
print(15-6) # subtraction
print(15*6) # multiplication
print(15/6) # division
print(15//6) # floor division (removes the decimal part)
print(15%6) # modulus (gives the remainder)
print(5**3) # exponentiation (5 to the power 3)