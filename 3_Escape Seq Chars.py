# escape sequence characters

# new line
print("hello\nAnindya")
# tab
print("hello\tworld")
# backslash
print("this is a backslash: \\")
# double quote escape
print("He said, \"Hello World!\"")
# string concatenation
print("Hi my name is " + "Anindya")

# parameters
# seperator
print("hi", "Anindya", "Dolui", sep="_")
# end
print("hello", "bhai", end="***") # next line starts from the same line
# or print("hello", "bhai", end="\n") next line starts from a new line

# single line comment (ctrl + /) shortcut
print("hi") #comment within a line after it ends
 
